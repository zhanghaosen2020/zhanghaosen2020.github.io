"""
演示python中的运算符
"""

# 算数运算符
print("1+1=",1+1)
print("45-2=",45-2)
print("45*2=",45*2)
print("46/2=",46/2)
print("9%2=",9%2)# 这是取余运算的符号%
print("9**2=",9**2)# 这是指数运算的符号**
print("11//2=",11//2)# 这是取整除的符号//
# 赋值运算符
num = 1+2*3
# 复合赋值运算符
num = 2
num += 1
print("num += 1",num)
num -= 1
print("num -= 1",num)
num *= 9
print("num *= 9",num)
num = 8
num /= 2
print("num /= 8",num)
num **= 3
print("num **=3",num)
